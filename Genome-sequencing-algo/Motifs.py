def Pr(Text, Profile):
    p = 1
    i = 0
    for item in Text:
        p = p*Profile[item][i]
        i = i+1
    return p


def ProfileMostProbablePattern(Text, k, Profile):
    P=[]
    for i in range(len(Text)-k+1):
        P.append(Pr(Text[i:i+k],Profile))
    start_index = P.index(max(P))
    return Text[start_index:start_index+k]


