import random

def Normalize(Probabilities):
    NormalizedP = Probabilities
    Sum = sum(Probabilities.values())
    for item in Probabilities:
        NormalizedP[item] = Probabilities[item]/Sum
    return NormalizedP


def WeightedDie(Probabilities):
    kmer = '' # output variable
    p = random.uniform(0, 1)
    for item in Probabilities:
        if p<= Probabilities[item]:
            kmer = item
            break
        else:
            p = p-Probabilities[item]
    return kmer

def ProfileGeneratedString(Text, profile, k):
    n = len(Text)
    probabilities = {}
    for i in range(0,n-k+1):
        probabilities[Text[i:i+k]] = Pr(Text[i:i+k], profile)
    probabilitiesN = Normalize(probabilities)
    return WeightedDie(probabilitiesN)


